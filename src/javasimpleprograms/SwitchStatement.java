package javasimpleprograms;

public class SwitchStatement {

	public static void main(String[] args) {
		int number = 10;
		//switch
		switch(number) {
		case 1:{
			System.out.println("10");
		}
		break;
		case 2:
			System.out.println("30");
			break;
		case 3:
			System.out.println("40");
			break;
		
			default:
				System.out.println("default");
		}
		

	}
}


